namespace Isics\Bundle\OpenMiamMiamUserBundle\EventListener;

use FOS\UserBundle\Event\FormEvent;
use FOS\UserBundle\FOSUserEvents;
use Symfony\Component\EventDispatcher\EventSubscriberInterface;
use Symfony\Component\HttpFoundation\RedirectResponse;
use Symfony\Component\Routing\Generator\UrlGeneratorInterface;

class ProfileEditingListener implements EventSubscriberInterface
{
    private $router;

    public function __construct(UrlGeneratorInterface $router)
    {
        $this->router = $router;
    }

    /**
     * {@inheritDoc}
     */
    public static function getSubscribedEvents()
    {
        return array(
            FOSUserEvents::PROFILE_EDIT_SUCCESS => 'onSuccess',
            FOSUserEvents::RESETTING_RESET_SUCCESS => 'onSuccess',
            FOSUserEvents::CHANGE_PASSWORD_SUCCESS => 'onSuccess',
        );
    }

    public function onSuccess(FormEvent $event)
    {
        $event->setResponse(new RedirectResponse($this->router->generate('open_miam_miam.homepage')));
    }
}
